export function t(key, lang) {
  const locale = lang || getCurrentLang();
  return (translations[locale] && translations[locale][key]) ||
         (translations['pt-BR'] && translations['pt-BR'][key]) ||
         key;
}

export function getCurrentLang() {
  return localStorage.getItem('arena-lang') || 'pt-BR';
}

export function applyTranslations(lang) {
  const locale = lang || getCurrentLang();

  // data-i18n — textContent
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const val = t(key, locale);
    if (val !== key) el.textContent = val;
  });

  // data-i18n-html — innerHTML (for elements with tags like <span>, <br>)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    const val = t(key, locale);
    if (val !== key) el.innerHTML = val;
  });

  // data-i18n-placeholder
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    const val = t(key, locale);
    if (val !== key) el.placeholder = val;
  });

  // data-i18n-aria-label
  document.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
    const key = el.dataset.i18nAriaLabel;
    const val = t(key, locale);
    if (val !== key) el.setAttribute('aria-label', val);
  });

  // Update HTML lang attribute
  document.documentElement.lang = locale;
}

export function initI18n() {
  const lang = getCurrentLang();
  if (lang !== 'pt-BR') {
    applyTranslations(lang);
  }
}
